#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*element;

	element = (t_list *)malloc(sizeof(t_list));
	if (!element)
		return ((void *)0);
	element->content = content;
	element->next = ((void *)0);
	return (element);
}
