#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*temp;

	temp = *lst;
	while (temp)
	{
		del(temp->content);
		temp = temp->next;
		free(*lst);
		*lst = temp;
	}
	free(*lst);
}
